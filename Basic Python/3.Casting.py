# TIPE DATA
# integer = bilangan tidak bulat
# float   = bilangan berkoma
# string  = karakter
# boolean = True(1) False(0)

# Integer
Data_Integer = 5
print("Data Integer = ", Data_Integer,type(Data_Integer))

# Float
Data_float = float(Data_Integer)
print("Data float = ", Data_float, type(Data_float))

# String
Data_string = str(Data_Integer)
print("Data string = ", Data_string, type(Data_string))

# Boolean
Data_Boolean = bool(Data_Integer)
print("Data boolean = ", Data_Boolean,type(Data_Boolean))

